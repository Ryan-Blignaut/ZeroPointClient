package github.thesivlerecho.zeropoint.gui.old.font;

public class FontPositionalHolder
{
	private int x, y, w, h;

	public FontPositionalHolder(int x, int y, int w, int h)
	{
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}


	public int getX()
	{
		return x;
	}


	public int getY()
	{
		return y;
	}


	public int getW()
	{
		return w;
	}


	public int getH()
	{
		return h;
	}

}
